Axiom elt : Type.
Axiom elt_inhabited : Inhabited elt.
Global Existing Instance elt_inhabited.
Axiom elt_countable : Countable elt.
Global Existing Instance elt_countable.
Axiom le : elt -> elt -> Prop.
Axiom Refl : forall  (x : elt), le x x.
Axiom Trans : forall  (x : elt) (y : elt) (z : elt) (fact0 : le x y) (fact1 : le y z), le x z.
Axiom Total : forall  (x : elt) (y : elt), le x y ∨ le y x.
Axiom t : Type.
Axiom t_inhabited : Inhabited t.
Global Existing Instance t_inhabited.
Axiom t_countable : Countable t.
Global Existing Instance t_countable.
Axiom elts : t -> list elt.
Axiom t'invariant : forall  (i1 : Z) (i2 : Z) (self : t) (fact0 : 0%Z ≤ i1) (fact1 : i1 ≤ i2) (fact2 : i2 < Z.of_nat (length (elts self))), le (nth (Z.to_nat i1) (elts self) inhabitant) (nth (Z.to_nat i2) (elts self) inhabitant).
